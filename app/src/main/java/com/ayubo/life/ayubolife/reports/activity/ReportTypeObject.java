package com.ayubo.life.ayubolife.reports.activity;

public class ReportTypeObject {

    String id;
    String name;

    public ReportTypeObject(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
